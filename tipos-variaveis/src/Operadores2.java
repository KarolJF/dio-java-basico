public class Operadores2 {
                public static void main(String[] args) {

                        int numero = 5;
                        //x repetição
                        //numero = numero +1
                        numero = numero +2;

                        //incrementar recorrencia de um mesmo valor

                        System.out.println(numero);

                        numero++;

                        System.out.println(numero);

                        System.out.println(numero ++);

                        System.out.println(++ numero);

                        System.out.println(numero --);

                        System.out.println(-- numero);

                }                     
}
